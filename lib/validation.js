var check = require('./check');

module.exports.objectHasProperties = function(propertyNames) {
    return         {
        getValue: function (object) {
            return object;
        },
        isMet: function (value) {
            return check.hasProperties(value, propertyNames);
        },
        error: 'Must have properties ' + propertyNames.join[' ']
    }
};

module.exports.propertyIsInRange = function(propertyName, min, max) {
    return         {
        getValue: function (object) {
            return object[propertyName]
        },
        isMet: function (value) {
            return check.isInRange(value, min, max);
        },
        error: propertyName + ' must be in range (' + min + ',' + max + ')'
    }
};

module.exports.propertyIsZeroOrOne = function(propertyName) {
    return         {
        getValue: function (object) {
            return object[propertyName]
        },
        isMet: function (value) {
            return check.isZeroOrOne(value);
        },
        error: propertyName + ' must be 0 or 1'
    }
};

module.exports.propertyHasValue = function(propertyName, expected) {
    return         {
        getValue: function (object) {
            return object[propertyName]
        },
        isMet: function (value) {
            return value === expected;
        },
        error: propertyName + ' must be ' + expected
    }
};

module.exports.getValidationErrors = function (object, rules) {
    var errors = [];

    rules.forEach(function (rule) {
        var value = rule.getValue(object);
        if (!rule.isMet(value)) {
            errors.push(rule.error);
        }
    });

    return errors;
};